// Arithmetic Operators
let x;

// Addition
x = 5 + 5;
console.log(x);

// Subtraction
x = 5 - 5;
console.log(x);

// Multiplication
x = 5 * 5;
console.log(x);

// Division
x = 5 / 5;
console.log(x);

// Modulus
x = 7 % 5;
console.log(x);

// Exponentation
x = 2 ** 3;
console.log(x);

// Concatenation Operators
x = 'Hello' + ', ' + 'World!';
console.log(x);

// Increment Operators
x = 1;
x = x + 1;
x += 1;
x++;
console.log(x);

// Decrement Operators
x = x - 1;
x -= 1;
x--;
console.log(x);

// Assignment Operators
x = 10;

x += 5;
console.log(x);

x -= 5;
console.log(x);

x *= 5;
console.log(x);

x /= 5;
console.log(x);

x %= 7;
console.log(x);

x **= 5;
console.log(x);

// Comparison Operators
x = 10 > 5;
console.log(x);

x = 10 < 5;
console.log(x);

x = 10 <= 5;
console.log(x);

x = 10 >= 5;
console.log(x);

// Equality Operators
// '==' operator just compares the value but not the type.
// '===' operator just compares the value as well as the type.
x = 2 == 2;
console.log(x);

x = 2 === '2';
console.log(x);

x = 2 != '2';
console.log(x);

x = 2 !== 2;
console.log(x);
